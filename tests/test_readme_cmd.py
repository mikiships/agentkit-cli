class TestRemoveSection:
    def test_remove_existing_section(self):
        """Remove deletes the injected section cleanly."""
        original = "# My Project\n\nSome text.\n"
        with_badge = inject_section(original, score=70)
        removed = remove_section(with_badge)
        assert "## Agent Quality" not in removed
        assert "[![agent quality]" not in removed
        assert "# My Project" in removed
        assert "Some text." in removed

    def test_remove_leaves_no_orphaned_blank_lines(self):
        """Remove doesn't leave double blank lines at EOF."""
        readme = "# My Project\n\nSome text.\n"
        with_badge = inject_section(readme, score=70)
        removed = remove_section(with_badge)
        # Should not end with multiple blank lines
        assert not removed.endswith("\n\n\n")

    def test_remove_no_section_is_noop(self):
        """Remove on README without section returns original content."""
        original = "# My Project\n\nNo badge here.\n"
        result = remove_section(original)
        assert result == original

    def test_remove_custom_header(self):
        """Remove respects custom section header."""
        original = "# Repo\n"
        with_badge = inject_section(original, score=60, section_header="## Quality Badge")
        removed = remove_section(with_badge, section_header="## Quality Badge")
        assert "## Quality Badge" not in removed
        assert "# Repo" in removed


