class TestReadmeCommand:
    def _make_readme(self, tmp_path: Path, content: str = "# My Project\n\nDescription.\n") -> Path:
        p = tmp_path / "README.md"
        p.write_text(content, encoding="utf-8")
        return p

    def test_inject_new_badge(self, tmp_path):
        """readme_command injects badge into README without existing badge."""
        readme = self._make_readme(tmp_path)
        readme_command(
            readme=readme,
            dry_run=False,
            remove=False,
            section_header="## Agent Quality",
            path=tmp_path,
            score_override=75,
        )
        content = readme.read_text(encoding="utf-8")
        assert "## Agent Quality" in content
        assert "75%2F100" in content

    def test_dry_run_does_not_modify_file(self, tmp_path):
        """--dry-run shows output but does not modify README.md."""
        readme = self._make_readme(tmp_path)
        original_content = readme.read_text(encoding="utf-8")
        readme_command(
            readme=readme,
            dry_run=True,
            remove=False,
            section_header="## Agent Quality",
            path=tmp_path,
            score_override=80,
        )
        assert readme.read_text(encoding="utf-8") == original_content

    def test_remove_flag(self, tmp_path):
        """--remove deletes an existing badge section."""
        readme = self._make_readme(tmp_path)
        # First inject
        readme_command(
            readme=readme,
            dry_run=False,
            remove=False,
            section_header="## Agent Quality",
            path=tmp_path,
            score_override=60,
        )
        assert "## Agent Quality" in readme.read_text(encoding="utf-8")
        # Then remove
        readme_command(
            readme=readme,
            dry_run=False,
            remove=True,
            section_header="## Agent Quality",
            path=tmp_path,
            score_override=None,
        )
        assert "## Agent Quality" not in readme.read_text(encoding="utf-8")

    def test_no_readme_found_exits_1(self, tmp_path):
        """readme_command exits with code 1 if README.md is missing."""
        import typer
        with pytest.raises(typer.Exit):
            readme_command(
                readme=tmp_path / "README.md",
                dry_run=False,
                remove=False,
                section_header="## Agent Quality",
                path=tmp_path,
                score_override=None,
            )

    def test_custom_readme_path(self, tmp_path):
        """--readme accepts a custom path."""
        custom = tmp_path / "docs" / "README.md"
        custom.parent.mkdir()
        custom.write_text("# Docs\n", encoding="utf-8")
        readme_command(
            readme=custom,
            dry_run=False,
            remove=False,
            section_header="## Agent Quality",
            path=tmp_path,
            score_override=55,
        )
        content = custom.read_text(encoding="utf-8")
        assert "55%2F100" in content

    def test_update_existing_badge(self, tmp_path):
        """readme_command updates score in existing badge section."""
        readme = self._make_readme(tmp_path)
        readme_command(
            readme=readme, dry_run=False, remove=False,
            section_header="## Agent Quality", path=tmp_path, score_override=50,
        )
        readme_command(
            readme=readme, dry_run=False, remove=False,
            section_header="## Agent Quality", path=tmp_path, score_override=95,
        )
        content = readme.read_text(encoding="utf-8")
        assert "95%2F100" in content
        assert "50%2F100" not in content
        assert content.count("## Agent Quality") == 1

    def test_empty_readme(self, tmp_path):
        """readme_command handles an empty README.md gracefully."""
        readme = tmp_path / "README.md"
        readme.write_text("", encoding="utf-8")
        readme_command(
            readme=readme, dry_run=False, remove=False,
            section_header="## Agent Quality", path=tmp_path, score_override=70,
        )
        content = readme.read_text(encoding="utf-8")
        assert "## Agent Quality" in content


