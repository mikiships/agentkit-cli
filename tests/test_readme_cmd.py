class TestInjectSection:
    def test_inject_new_badge_into_empty_readme(self):
        """Inject into an empty README appends a section."""
        result = inject_section("", score=75, section_header="## Agent Quality")
        assert "## Agent Quality" in result
        assert "[![agent quality]" in result
        assert "75%2F100" in result

    def test_inject_new_badge_appends_to_existing_content(self):
        """Inject into README with existing content preserves original content."""
        original = "# My Project\n\nSome description here.\n"
        result = inject_section(original, score=82)
        assert "# My Project" in result
        assert "Some description here." in result
        assert "## Agent Quality" in result
        assert "82%2F100" in result

    def test_inject_adds_blank_line_separator(self):
        """Injected section is separated from existing content by a blank line."""
        original = "# My Project\n"
        result = inject_section(original, score=50)
        # There should be a blank line before the section header
        assert "\n\n## Agent Quality" in result

    def test_update_existing_badge_score(self):
        """Update replaces the old score with the new one."""
        original = inject_section("# Repo\n\n", score=50)
        updated = inject_section(original, score=90)
        assert "90%2F100" in updated
        assert "50%2F100" not in updated
        assert "# Repo" in updated

    def test_idempotent_same_score(self):
        """Calling inject with the same score twice is idempotent."""
        base = "# My Project\n\n"
        first = inject_section(base, score=70)
        second = inject_section(first, score=70)
        # Content should be the same (no duplicate sections)
        assert second.count("## Agent Quality") == 1
        assert second.count("[![agent quality]") == 1

    def test_custom_section_header(self):
        """Custom section header is used instead of default."""
        result = inject_section("# Repo\n", score=60, section_header="## Quality Badge")
        assert "## Quality Badge" in result
        assert "## Agent Quality" not in result

    def test_generated_footer_present(self):
        """Injected section includes the generated-by footer."""
        result = inject_section("", score=80)
        assert "Generated by [agentkit-cli]" in result

    def test_inject_preserves_trailing_content(self):
        """Inject preserves content after the injected section (update mode)."""
        section = build_section(50)
        readme = "# Intro\n\n" + section + "\n## Another Section\n\nContent here.\n"
        updated = inject_section(readme, score=85)
        assert "## Another Section" in updated
        assert "Content here." in updated

    def test_badge_url_color_green(self):
        """Score >= 80 produces green badge."""
        result = inject_section("", score=85)
        assert "green" in result

    def test_badge_url_color_yellow(self):
        """Score 60-79 produces yellow badge."""
        result = inject_section("", score=65)
        assert "yellow" in result

    def test_badge_url_color_red(self):
        """Score < 40 produces red badge."""
        result = inject_section("", score=20)
        assert "red" in result


